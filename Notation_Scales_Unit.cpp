//------------------------------------------------------------------------------
// Название:  fn_P_t_P_t_en_i_Delete.
// Класс:     T_Cl_Digital_Array.
// Описание:  освобождение динамической памяти из-под массива разрядов числа.
//------------------------------------------------------------------------------
void T_Cl_Digital_Array :: fn_P_t_P_t_en_i_Delete()
{
  // Если определён (не равен NULL) указатель на адрес
  // начала массива разрядов числа, то число уже существует (память не зачищена)
  // и, возможно, определены какие-то указатели на цифры,
  // то занятую память надо освободить.
  if (F_P_t_P_t_en_i_Digits != NULL)
  {
    // Освобождаются разряды, зарезервированные под символы цифр заданного числа.
    for (unsigned short ush = 0; ush < F_ush_Length; ush++)
      // Если указатель не равен NULL, то память освобождается.
      if (F_P_t_P_t_en_i_Digits[ush] != NULL)
      {
        // Освобождение динамической памяти из-под символа цифры.
        delete F_P_t_P_t_en_i_Digits[ush];
        // Указатель данного разряда ссылается в никуда.
        F_P_t_P_t_en_i_Digits[ush] = NULL;
      } // if

    // Освобождение непрерывной облати динамической памяти
    // из-под массива указателей на цифры числа.
    delete[] F_P_t_P_t_en_i_Digits;
    F_P_t_P_t_en_i_Digits = NULL;
  } // if
} // T_Cl_Digital_Array :: fn_P_t_P_t_en_i_Delete


