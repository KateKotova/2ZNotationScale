// К о н с т а н т н а я   ф у н к ц и я .
//------------------------------------------------------------------------------
// Название:  fn_sh_Get_t_Cl_Decimal_Equivalent.
// Класс:     T_Cl_Notation_Scale_Number_Order.
// Описание:  получение десятичного эквивалента числа.
// Результат: данное число, переведённое в десятичную систему счисления.
//------------------------------------------------------------------------------
short
  T_Cl_Notation_Scale_Number_Order :: fn_sh_Get_t_Cl_Decimal_Equivalent() const
{
  // Данное число в десятичной системе счисления.
  short sh_Decimal_Equivalent = 0;

  // Просмотр всех разрядов числа.
  for (unsigned char uch = 0;
       uch < F_P_t_Cl_Digital_Array->pr_ush_Length; uch++)
    // Вычисление десятичного эквивалента по формуле:
    // сумма произведений значений разрядов и основания системы счисления
    // в степенях номеров разрядов числа.
    sh_Decimal_Equivalent +=
      (short)*(F_P_t_Cl_Digital_Array->F_P_t_P_t_en_i_Digits[uch]) *
      pow(F_P_t_Cl_Notation_Scale->pr_t_en_i_Base, uch);

  // Установка знака числа: если порядок отрицателен, то его эквивалент - тоже.
  if (! F_b_Positive_Sign_Flag)
    sh_Decimal_Equivalent *= -1;

  // Возврат полученного числа.
  return sh_Decimal_Equivalent;
} // T_Cl_Notation_Scale_Number_Order :: fn_sh_Get_t_Cl_Decimal_Equivalent


//==============================================================================
// Ф у н к ц и я .
//------------------------------------------------------------------------------
// Название:  fn_Set_sh_Value.
// Класс:     T_Cl_Notation_Scale_Number_Order.
// Описание:  перевод десятичного числа в данную систему счисления
//            и присвоение его данному числу.
// Параметры:
//   sh_Value - число в десятичной системе счисление, присваиваемое данному.
//------------------------------------------------------------------------------
void T_Cl_Notation_Scale_Number_Order :: fn_Set_sh_Value(short sh_Value)
{
  // При параметре, большем максимально допустимого порядка
  // в двоичной системе счисления.
  if (sh_Value > ush_Binary_Notation_Scale_Order)
  {
    // Установка числа в состояние переполнения.
    pr_b_Overflow_Flag = true;
    // Возврат из функции.
    return;
  } // if

  // При нулевом параметре число устанавливается в нуль (+0).
  if (! sh_Value)
  {
    pr_b_Zero_Flag = true;
    // Возврат из функции.
    return;
  } // if

  // При ненулевом аргументе.
  // Запись знака числа.
  if (sh_Value > 0)
    F_b_Positive_Sign_Flag = true;
  else
  {
    F_b_Positive_Sign_Flag = false;
    // Остаётся только модуль числа для дальнейшей проверки.
    sh_Value *= -1;
  } // else

  // Накапливаемый массив разрядов результирующего числа,
  // чья длина предварительно устанавливается максимальной
  // в двоичной системе счисления.
  T_Cl_Digital_Array *P_t_Cl_Digital_Array =
    new T_Cl_Digital_Array(uch_Binary_Notation_Scale_Order_Length);

  // Счётчик разрядов предварительного числа,
  // отсчёт ведётся от младшего разряда к старшим.
  unsigned char uch = 0;

  // Получение искомого числа следующим образом:
  // последовательное целочисленное деление аргумента
  // на основание системы счисления с записью остатнков
  // и последнего частного, меньшего, чем основание данной системы счисленя;
  // инверсия полученной последовательности цифр данной системы счисления
  // из остатков от деления и последнего частного.
  while (abs(sh_Value) >= F_P_t_Cl_Notation_Scale->pr_t_en_i_Base)
  {
    // Результат целочисленного деления с остатком (целая часть и остаток)
    // от деления текущего значения десятичного числа
    // на основание данной системы счисления -
    // структура из двух целочисленных полей.
    div_t st_i_Remainder_Division =
      div(sh_Value, F_P_t_Cl_Notation_Scale->pr_t_en_i_Base);

    // Запись цифры данной системы счисленя, соответствующей значению остатка
    // от целочисленного деления, в текущий разряд числа.
    *(P_t_Cl_Digital_Array->F_P_t_P_t_en_i_Digits[uch]) =
      (T_en_i_Figure) st_i_Remainder_Division.rem;

    // Новое значение делимого - полученное целочисленное частное.
    sh_Value = st_i_Remainder_Division.quot;

    // Инкремент текущего разряда числа.
    uch++;
  } // while

  // Запись последнего частного, меньшего основания системы счисления.
  *(P_t_Cl_Digital_Array->F_P_t_P_t_en_i_Digits[uch]) =
      (T_en_i_Figure) sh_Value;

  // Усечение ведущих нулей полученного числа.
  P_t_Cl_Digital_Array =
    fn_P_t_Cl_Remove_t_Cl_Leading_Zeroes(P_t_Cl_Digital_Array);

  // Если длина полученного числа превышает максимально допустимую
  // для данной системы счисления, то число переполнено.
  if (P_t_Cl_Digital_Array->pr_ush_Length >
      F_P_t_Cl_Digital_Array->pr_ush_Length)
  {
    pr_b_Overflow_Flag = true;
    // Возврат из функции.
    return;
  } // if

  // Увеличение длины массива разрядов числа
  // за счёт добавления ведущих нулей.
  P_t_Cl_Digital_Array =
    fn_P_t_Cl_Add_t_Cl_Leading_Zeroes(P_t_Cl_Digital_Array,
                                      F_P_t_Cl_Digital_Array->pr_ush_Length);

  // Запись полученного числа в данное.
  *F_P_t_Cl_Digital_Array = *P_t_Cl_Digital_Array;

  // Удаление полученного числа - промежуточного расчёта.
  delete P_t_Cl_Digital_Array;
  // Указатель ссылается в никуда.
  P_t_Cl_Digital_Array = NULL;
} // T_Cl_Notation_Scale_Number_Order :: fn_Set_sh_Value


