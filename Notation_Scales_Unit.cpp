// К о н с т а н т н а я   ф у н к ц и я .
//------------------------------------------------------------------------------
// Название:  fn_uch_Get_as_String_ld_Decimal_Equivalent.
// Класс:     T_Cl_Notation_Scale.
// Описание:  перевод числа, цифры которого, принадлежащие
//            данной системе счисления, отображены символами заданной cтроки,
//            в десятичную систему счисления, форматирование заданной строки
//            и сигнализация об успености выполнения функции.
// Параметры:
//   as_String                    - некоторая строка, которая форматируется
//                                  и чьи символы проверяются на идентичность
//                                  символам цифр данной системы счисления.
//   sh_Number_Decimal_Equivalent - десятичное число, эквивалентное числу
//                                  данной системы счисления, записанному
//                                  в строке, переданной в качестве параметра.
// Результат: ответ на вопрос: осуществим ли перевод числа,
//            представленного в заданной строке, в десятичную систему счисления:
//   0 - да;
//   1 - нет, строка содержит недопустимые смволы;
//   2 - да, но возникает переполнение, то есть полученное число
//       не может быть представленно типом long double;
//   3 - нет при прочих ошибках.
//------------------------------------------------------------------------------
unsigned char T_Cl_Notation_Scale :: fn_uch_Get_as_String_ld_Decimal_Equivalent
  (AnsiString &as_String,
   long double &ld_String_Decimal_Equivalent) const
{
  // Признак положительности искомого десятичного эквивалента числа.
  bool b_String_Decimal_Equivalent_Positive_Sign_Flag = true;

  // Удаление ведущих пробелов строки.
  as_String = as_String.TrimLeft();

  // Анализ первого символа: если он представляет собой знак минуса,
  // то результирующее число, скорее всего будет отрицательным,
  // иначе - положительным.
  if (as_String[1] == ch_Minus)
    // Число-эквивалент будет отрицательным.
    b_String_Decimal_Equivalent_Positive_Sign_Flag = false;

  // Найденный знак минуса или плюса удаляется для дальнейшего анализа
  // только символов, обозначающих цифры - получение подстроки
  // со второго символа, количество символов уменьшптся на один.
  if ((as_String[1] == ch_Minus) || (as_String[1] == ch_Plus))
    as_String = as_String.SubString(2, as_String.Length() - 1);

  // Форматирование заданной строки: удаление ведущих и завершающих пробелов
  // и ведущих нулей, усечение строки до длины максимальной мантиссы
  // плюс один символ для округления и получение признака вхождения
  // оставшихся символов во множество цифр данной системы счисления.
  // Если все символы заданной строки входят в алфавит данной системы счисления,
  // то осуществляется перевод заданной строки в число,
  // инначе результирующее число обнуляется.
  if (fn_b_Get_as_Correct_Figures_String_Flag(as_String))
  {
    try
    {
      // Заданное число в десятичной системе счисления обнуляется.
      ld_String_Decimal_Equivalent = 0;

      // Просмотр всех разрядов числа в строке цифр от младшего разряда,
      // расположенного в конце строки, до старшего - в начале,
      // получение индекса разряда числа, записанного в строке.
      for (int i_String_Char_Index   = as_String.Length(),
               i_Digital_Array_Digit = 0;
           i_String_Char_Index > 0;
           i_String_Char_Index--, i_Digital_Array_Digit++)
      {
        // Цифра данной системы счисления, эквивалентная текущему символу.
        T_en_i_Figure t_en_i_String_Char_Figure;

        // Поиск цифра-эквивалента в данной системе счисления
        // для текущего симаола заданной строки.
        fn_b_Get_ch_Char_t_en_i_Figure(as_String[i_String_Char_Index],
                                       t_en_i_String_Char_Figure);

        // Вычисление десятичного эквивалента по формуле:
        // сумма произведений значений разрядов и основания системы счисления
        // в степенях номеров разрядов числа.
        ld_String_Decimal_Equivalent += (int) t_en_i_String_Char_Figure *
          pow(F_t_en_i_Base, i_Digital_Array_Digit);
      } // for

     // Если признак положительности искомого
     // десятичного эквивалента числа ложен, но оно становится отрицательным.
     if (! b_String_Decimal_Equivalent_Positive_Sign_Flag)
       ld_String_Decimal_Equivalent *= -1;

     // Возврат признака удачного выполнения функции - нуля.
     return 0;
    } // try

    // Прехват исключения переполнения разарядной сетки.
    catch (EOverflow&)
    {
      // Запись нуля в десятичный эквивалент заданного числа,
      // хотя это значение не имеет смысла, ибо возникло переполнение
      // разарядной сетки в резельтате накопления результата
      // при суммировании и/или умножении.
      ld_String_Decimal_Equivalent = 0;

      // Возврат кода ошибки - 1.
      return 2;
    } // catch (EOverflow&)

    // Прехват прочих исключений.
    catch (...)
    {
      // Запись нуля в десятичный эквивалент заданного числа, хотя это значение
      // не имеет смысла, ибо имело место исключительная ситуация.
      ld_String_Decimal_Equivalent = 0;

      // Возврат кода ошибки - 3.
      return 3;
    } // catch (...)
  } // if

  // В заданной строке имеется символ, который не определён
  // в качестве символьного представления для данной системы счисления.
  else
  {
    // Запись нуля в десятичный эквивалент заданного числа,
    // хотя это значение не имеет смысла, ибо нет десятичного эквивалента
    // заданного числа в данной системе счисления при наличие
    // в его строковом представлении недоступных символов.
    ld_String_Decimal_Equivalent = 0;

    // Возврат кода ошибки - 1.
    return 1;
  } // else
} // T_Cl_Notation_Scale :: fn_uch_Get_as_String_ld_Decimal_Equivalent


