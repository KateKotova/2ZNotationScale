//------------------------------------------------------------------------------
// Название:  fn_ush_Get_ld_Integer_Positive_Number_Length.
// Класс:     T_Cl_Notation_Scale.
// Описание:  получение максимальной длины некоторого
//            большого целого положительного числа в данной системе счисления.
// Параметры:
//   ld_Integer_Positive_Number - некоторое целое положительное число
//                                в десятиной системе счиления, чья длина
//                                определяется в данной системе счисления.
// Результат: максимальная длина переданного в качестве параметра числа
//            в данной системе счисления.
//------------------------------------------------------------------------------
unsigned short
  T_Cl_Notation_Scale :: fn_ush_Get_ld_Integer_Positive_Number_Length
    (long double ld_Integer_Positive_Number) const
{
  // Округление заданного числа вверх для избавления от дробной части
  // и получение его модуля для возможности представления
  // в качестве подлогарифмического выражения;
  // функции ceill и fabsl специально предназначены для типa long double.
  ld_Integer_Positive_Number = fabsl(ceill(ld_Integer_Positive_Number));

  // Если полученное неотрицательное число равно нулю - меньше единицы,
  // то его нельзя использовать в качестве подлогарифмического выражения,
  // тем более, что требуется всего один разряд для его хранения.
  if (ld_Integer_Positive_Number < 1)
    return 1;

  else
    // Необходимое число разрядов в данной системе счисления
    // для представления конкретного числа
    // определяется, как округлённый вверх логарифм этого числа
    // по основанию числа, определяющего данную систему счисления
    // (поскольку в файле "math.h" не определена функция
    // вычисления логарифма по произвольному снованию,
    // то можно воспользоваться формулой перехода к другому основанию:
    // логарифм b по основанию a равен частному логарифма b по основанию с
    // и логарифма а пооснованию с, здесь с принято равным десяти;
    // функция log10l специально предназначена для типa long double).
    return ceil(log10l(ld_Integer_Positive_Number) / log10 (F_t_en_i_Base));
} // T_Cl_Notation_Scale :: fn_ush_Get_ld_Integer_Positive_Number_Length


