//==============================================================================
// Ф у н к ц и я .
//------------------------------------------------------------------------------
// Название:  fn_Get_t_a_P_t_en_i_Rounded.
// Класс:     T_Cl_Notation_Scale_Number_Whole_Part.
// Описание:  получение окуглённого числа до некоторого количества разрядов.
// Параметры:
//   uch_Number_Significant_Digits - максимальное количество занчащих разрядов,
//                                   которое должно содержать число.
//------------------------------------------------------------------------------
void T_Cl_Notation_Scale_Number_Whole_Part :: fn_Get_t_a_P_t_en_i_Rounded
  (unsigned char &uch_Number_Significant_Digits)

{
  // Если количество занчащих разрядов - нуль, по ошибке,
  // то оно устанавливается в минимальное - единицу.
  if (uch_Number_Significant_Digits == 0)
    uch_Number_Significant_Digits = 1;

  // Если количество занчащих разрядов превышает установленную
  // максимкальную точность представления числа для данной системы счисления,
  // то количество значащих разрядов устанавливается как максимально допустимое.
  if (uch_Number_Significant_Digits >
        F_P_t_Cl_Notation_Scale->pr_uch_Number_Mantissa_Length)
    uch_Number_Significant_Digits =
      F_P_t_Cl_Notation_Scale->pr_uch_Number_Mantissa_Length;

  // Максимальный занчащий разряд числа.
  short sh_Digital_Array_Most_Significant_Digit =
          F_P_t_Cl_Digital_Array->pr_sh_Most_Significant_Digit;

  // Если количество разрядов до максимального значищего разряда
  // окажется меньше или равно запрашиваемой точности представления числа,
  // то оно остаётся без изменения.
  if (uch_Number_Significant_Digits >=
      (sh_Digital_Array_Most_Significant_Digit + 1))
    // Возврат из функции.
    return;

  // Перенос - цифра, прибавляемая к старшему разряду числа:
  // если в результате суммировани значения текущего разряда
  // и некоторого числа (здесь это предыдущий перенос) получается число,
  // большее, либо равное основанию системы счисления,
  // то в текущий разряд записывается остаток от деления суммы
  // на основание системы счисления, а перенос - частное
  // целочисленного деления суммы на основание системы счисления.
  bool b_Carry;

  if (*(F_P_t_Cl_Digital_Array->F_P_t_P_t_en_i_Digits
        [sh_Digital_Array_Most_Significant_Digit -
         uch_Number_Significant_Digits]) >=
           ceil(F_P_t_Cl_Notation_Scale->pr_t_en_i_Base / 2))
    b_Carry = true;

  else
    b_Carry = false;

  // Последовательно во все разряды числа до поля, содержащего мантиссу,
  // заносятся нулевые символы - число усекается до этого поля.
  for (unsigned short ush = 0;
       ush < sh_Digital_Array_Most_Significant_Digit -
             uch_Number_Significant_Digits; ush++)
    *(F_P_t_Cl_Digital_Array->F_P_t_P_t_en_i_Digits[ush]) = i_0;


  // Если признак переноса ложен,
  // то полученное усечённое число уже является результатом.
  if (! b_Carry)
    // Возврат из функции.
    return;

  // Просмотр всех разрядов числа.
  for (unsigned short ush = sh_Digital_Array_Most_Significant_Digit -
                            uch_Number_Significant_Digits + 1;
       ush <= sh_Digital_Array_Most_Significant_Digit; ush++)
  {
    // Если при суммировании с переносом текущий разряд становитсЯ больше
    // основанея данной системы счисления.
    if (( *(F_P_t_Cl_Digital_Array->F_P_t_P_t_en_i_Digits[ush]) + b_Carry) >=
              F_P_t_Cl_Notation_Scale->pr_t_en_i_Base)
    {
       // В текущий разряд заносится разность суммы текущего разряда и переноса
       // и сонования данной систмы счисления.
       *(F_P_t_Cl_Digital_Array->F_P_t_P_t_en_i_Digits[ush]) = (T_en_i_Figure)
           ((int) *(F_P_t_Cl_Digital_Array->F_P_t_P_t_en_i_Digits[ush]) +
            (int) b_Carry -
            (int) F_P_t_Cl_Notation_Scale->pr_t_en_i_Base);
      // Поэтому новый перенос - в следующий разряд - истинен.
      b_Carry = true;
    } // if

    // Иначе текущий разряд просто суммируется с переносом.
    else
    {
      *(F_P_t_Cl_Digital_Array->F_P_t_P_t_en_i_Digits[ush]) = (T_en_i_Figure)
        ((int) *(F_P_t_Cl_Digital_Array->F_P_t_P_t_en_i_Digits[ush]) +
         (int) b_Carry);
      // Поскольку сумма не превысила основание данной системы счисления,
      // перенос в следующий разряд не осуществляется.
      b_Carry = false;
    } // else
  } // for
  
  // Если старший значищий разряд числа не расположен в последнем,
  // самом старшем разряде поля длины выделенной под данное число,
  // то в разряд, следующий за максимальным значащим, записывается
  // последний перенос, так как он, не будучи занчащим, был равен нулю.
  if (sh_Digital_Array_Most_Significant_Digit <
      (F_P_t_Cl_Digital_Array->pr_ush_Length - 1))
  {
     *(F_P_t_Cl_Digital_Array->
         F_P_t_P_t_en_i_Digits[sh_Digital_Array_Most_Significant_Digit + 1]) =
           (T_en_i_Figure) b_Carry;
    // Возврат из функции.
    return;
  } // if

  // Если старший значищий разраяд находится в самомконце числа
  // а признак переноса из него истинен, то так как не хватает
  // ещё одного разряда для записи этого переноса,
  // число устанавливается в состояние переполнения.
  if (! (b_Carry && (sh_Digital_Array_Most_Significant_Digit ==
                    (F_P_t_Cl_Digital_Array->pr_ush_Length - 1))))
    // Установка числа в состояние переполнения.
    pr_b_Overflow_Flag = true;
} // T_Cl_Notation_Scale_Number_Whole_Part :: fn_Get_t_a_P_t_en_i_Rounded


