//==============================================================================
// К о н с т а н т н а я   ф у н к ц и я .
//------------------------------------------------------------------------------
// Название:  fn_b_Get_P_t_P_t_en_i_NULLs_Flag.
// Класс:     T_Cl_Digital_Array.
// Описание:  получение признака неопределённости (равенства NULL)
//            указателя на адрес начала массива разрядов числа
//            или хотя бы одного указателя на цифру.
// Результат: ответ на вопрос: равен ли NULL указатель на адрес
//            начала массива разрядов числа или хотя бы один указатель на цифру.
//------------------------------------------------------------------------------
bool __fastcall T_Cl_Digital_Array :: fn_b_Get_P_t_P_t_en_i_NULLs_Flag() const
{
  // Если указатель на адрес начала массива разрядов числа равен NULL,
  // то ответ положительный.
  if (F_P_t_P_t_en_i_Digits == NULL)
    // Возврат положиельного ответа.
    return true;

  // Счётчик разрядов числа, отсчёт ведётся от младшего разряда к старшим.
  unsigned short ush = 0;

  // Пока указатель на цифру текущего разряда не ссылается на NULL
  // и текущий разряд не стал равным длине числа (то есть пока не просмотрены
  // все разряды числа, которые нумеруются, начиная с нуля),
  // осуществсяется переход для просмотра следующего разряда.
  while ((F_P_t_P_t_en_i_Digits[ush] != NULL) && (ush < F_ush_Length))
    ush++;

  // Если последний номер разряда равен длине числа, то были просмотрены
  // все разряды числа от нулевого до (F_ush_Length - 1)-го,
  // но значения NULL среди них обнаружено не было,
  // то в числе нет указателей на NULL,
  // иначе существует, по крайней мере, хотя бы один такой разряд.
  if (ush == F_ush_Length)
    // Возврат отрицательного ответа.
    return false;

  else
    // Возврат положиельного ответа.
    return true;
} // T_Cl_Digital_Array :: fn_b_Get_t_a_P_t_en_i_Number_NULL_Figures_Flag


//==============================================================================
// К о н с т а н т н а я   ф у н к ц и я .
//------------------------------------------------------------------------------
// Название:  fn_b_Get_P_t_P_t_en_i_Zero_Flag.
// Класс:     T_Cl_Digital_Array.
// Описание:  получение признака равенства числа нулю.
// Результат: ответ на вопрос: равно ли число нулю.
//------------------------------------------------------------------------------
bool __fastcall T_Cl_Digital_Array :: fn_b_Get_P_t_P_t_en_i_Zero_Flag() const
{
  // Если не определён (равен NULL) указатель на адрес
  // начала массива разрядов числа или хотя бы один указатель на цифру,
  // то нельзя сказать, что число равно нулю, поэтому ответ отрицателен.
  if (pr_b_NULLs_Flag)
    // Возврат отрицательного ответа.
    return false;

  // Счётчик разрядов числа, отсчёт ведётся от младшего разряда к старшим.
  unsigned short ush = 0;

  // Пока цифра текущего разряда равна нулю и он не стал равным
  // длине числа (то есть пока не просмотрены все разряды числа,
  // которые нумеруются, начиная с нуля),
  // осуществсяется переход для просмотра следующего разряда.
  while ((ush < F_ush_Length) && (*F_P_t_P_t_en_i_Digits[ush] == i_0))
    ush++;

  // Если последний номер разряда равен длине числа, то были просмотрены
  // все разряды числа от нулевого до (F_ush_Length - 1)-го,
  // но ненулевого среди них обнаружено не было,
  // то число равно нулю, иначе - не равно.
  if (ush == F_ush_Length)
    // Возврат положиельного ответа.
    return true;

  else
    // Возврат отрицательного ответа.
    return false;
} // T_Cl_Digital_Array :: fn_b_Get_P_t_P_t_en_i_Zero_Flag


//==============================================================================
// Ф у н к ц и я .
//------------------------------------------------------------------------------
// Название:  fn_Set_P_t_P_t_en_i_Zero_Flag.
// Класс:     T_Cl_Digital_Array.
// Описание:  установка числа в нуль при равенстве параметра true.
// Параметры:
//   b_Zero_Flag - новый признак равенства числа нулю.
//------------------------------------------------------------------------------
void __fastcall T_Cl_Digital_Array :: fn_Set_P_t_P_t_en_i_Zero_Flag
  (bool b_Zero_Flag)
{
  // Если новый признак нулевого значения числа равен true,
  // то число обнуляется, иначе число остаётся неизменным.
  if (b_Zero_Flag)
  {
    // Если указатель на адрес начала массива разрядов числа равен NULL,
    // то число не определено, поэтому следует выделить динамическую память
    // под массив разрядов числа и проинициализировать их нулями.
    if (F_P_t_P_t_en_i_Digits == NULL)
    {
      // Выделение динамической памяти под массив разрядов числа
      // и инициализация их нулями.
      fn_P_t_P_t_en_i_New();

      // Возврат из функции.
      return;
    } // if

    // Последовательно во все разряды числа заносятся нулевые символы.
    for (unsigned short ush = 0; ush < F_ush_Length; ush++)
    {
      // Если указатель текущего разряда равен NULL,
      // то под него выделяется динамическая память
      // и он инициализируется нулём.
      if (F_P_t_P_t_en_i_Digits[ush] == NULL)
        F_P_t_P_t_en_i_Digits[ush] = new T_en_i_Figure(i_0);

      else
        // В разряд просто записывается нуль.
        *F_P_t_P_t_en_i_Digits[ush] = i_0;
    } // for
  } // if
} // T_Cl_Digital_Array :: fn_Set_P_t_P_t_en_i_Zero_Flag


//==============================================================================
// К о н с т а н т н а я   ф у н к ц и я .
//------------------------------------------------------------------------------
// Название:  fn_sh_Get_P_t_P_t_en_i_Most_Significant_Digit.
// Класс:     T_Cl_Digital_Array.
// Описание:  получение старшего значищего разряда числа.
// Результат: номер старшего значащего разряда числа.
//------------------------------------------------------------------------------
short __fastcall
  T_Cl_Digital_Array :: fn_sh_Get_P_t_P_t_en_i_Most_Significant_Digit() const
{
  // Если заданное число частично или полностью не определено или равно нулю,
  // то поле старшего значищего разряда не играет роли и устанавливается в нуль,
  // иначе происходит поиск старшего ненулевого разряда.
  if (pr_b_NULLs_Flag || pr_b_Zero_Flag)
    // Возврат номера нулевого разряда.
    return 0;

  else
  {
    // Счётчик разрядов заданного числа,
    // отсчёт ведётся от старшего разряда к младшим.
    unsigned short ush = F_ush_Length - 1;

    // Пока цифра текущего разряда равна нулю,
    // осуществсяется переход для просмотра следующего разряда.
    while (*F_P_t_P_t_en_i_Digits[ush] == i_0)
      ush--;

    // Возврат искомого номера старшего ненулевой разряда,
    // который имеет последний полученный номер.
    return ush;
  } // else
} // T_Cl_Digital_Array :: fn_sh_Get_P_t_P_t_en_i_Most_Significant_Digit


//==============================================================================
// Д р у ж е с т в е н н а я   ф у н к ц и я .
//------------------------------------------------------------------------------
// Название:  fn_P_t_Cl_Remove_t_Cl_Leading_Zeroes.
// Класс:     T_Cl_Digital_Array.
// Описание:  уменьшение длины массива разрядов числа
//            за счёт удаления ведущих нулей.
// Параметры:
//   t_Cl_Digital_Array - массив разрядов числа, где удаляются ведущие нули.
// Результат: данный массив разрядов числа без ведущих нулей.
//------------------------------------------------------------------------------
T_Cl_Digital_Array *fn_P_t_Cl_Remove_t_Cl_Leading_Zeroes
  (T_Cl_Digital_Array *t_Cl_Digital_Array)
{
  // Если число частично илиполностью неопределено,
  // то оно остаётся без изменения - нет смысла операции.
  if (t_Cl_Digital_Array->pr_b_NULLs_Flag)
    // Возврат данного массива разрядов числа.
    return t_Cl_Digital_Array;

  // Если число равно нулю, то есть все разряды содержат нули,
  // то следует оставить только один нуль, занимающий всего один разряд.
  if (t_Cl_Digital_Array->pr_b_Zero_Flag)
    // Создание нового массива длиной в один разряд (по умолчанию)
    // и инициализация его нулём.
    return &T_Cl_Digital_Array();

  // Новое число - массив разрядов данного числа без ведущих нулей.
  T_Cl_Digital_Array *t_Cl_Removed_Leading_Zeroes_Digital_Array =
    new T_Cl_Digital_Array(t_Cl_Digital_Array->F_ush_Length);
  // Копирование значения числа в новое.
  *t_Cl_Removed_Leading_Zeroes_Digital_Array = *t_Cl_Digital_Array;

  // Cтарший значищий разряд числа.
  short sh_Digital_Array_Most_Significant_Digit =
    t_Cl_Digital_Array->pr_sh_Most_Significant_Digit;

  // Удаление ведущих нулей нового числа.
  for (unsigned short ush = t_Cl_Digital_Array->F_ush_Length - 1;
       ush > sh_Digital_Array_Most_Significant_Digit; ush--)
  {
    // Освобождение динамической памяти из-под символа цифры нового числа
    delete t_Cl_Removed_Leading_Zeroes_Digital_Array->
             F_P_t_P_t_en_i_Digits[ush];
    // Указатель текущего разряда нового числа ссылается в никуда.
    t_Cl_Removed_Leading_Zeroes_Digital_Array->F_P_t_P_t_en_i_Digits[ush] =
      NULL;
  } // for

  // Усечение длины нового числа.
  t_Cl_Removed_Leading_Zeroes_Digital_Array->F_ush_Length =
    sh_Digital_Array_Most_Significant_Digit + 1;

  // Возврат нового числа.
  return t_Cl_Removed_Leading_Zeroes_Digital_Array;
} // fn_P_t_Cl_Remove_t_Cl_Leading_Zeroes


//==============================================================================
// Д р у ж е с т в е н н а я   ф у н к ц и я .
//------------------------------------------------------------------------------
// Название:  fn_P_t_Cl_Remove_t_Cl_Terminating_Zeroes.
// Класс:     T_Cl_Digital_Array.
// Описание:  удаление завершающих нулей путём сдвига значищих разрядов
//            в младшие разряды.
// Параметры:
//   t_Cl_Digital_Array - массив разрядов числа, где удаляются завершающие нули.
// Результат: данный массив разрядов числа без завершающих нулей.
//------------------------------------------------------------------------------
T_Cl_Digital_Array *fn_P_t_Cl_Remove_t_Cl_Terminating_Zeroes
  (T_Cl_Digital_Array *t_Cl_Digital_Array)
{
  // Если число частично или полностью неопределено,
  // то оно остаётся без изменения - нет смысла операции.
  if (t_Cl_Digital_Array->pr_b_NULLs_Flag)
    // Возврат данного массива разрядов числа.
    return t_Cl_Digital_Array;

  // Если число равно нулю, то есть все разряды содержат нули,
  // то следует оставить только один нуль, занимающий всего один разряд.
  if (t_Cl_Digital_Array->pr_b_Zero_Flag)
    // Создание нового массива длиной в один разряд (по умолчанию)
    // и инициализация его нулём.
    return &T_Cl_Digital_Array();

  // Новое число - массив разрядов данного числа без завершающих нулей.
  T_Cl_Digital_Array *t_Cl_Removed_Terminating_Zeroes_Digital_Array =
    new T_Cl_Digital_Array(t_Cl_Digital_Array->F_ush_Length);
  // Копирование значения числа в новое.
  *t_Cl_Removed_Terminating_Zeroes_Digital_Array = *t_Cl_Digital_Array;

  // Младший ненулевой разряд числа.
  short sh_Digital_Array_Least_Nonzero_Digit = 0;

  // Пока цифра текущего разряда равна нулю,
  // осуществсяется переход для просмотра следующего разряда.
  while ((sh_Digital_Array_Least_Nonzero_Digit <
          t_Cl_Digital_Array->pr_ush_Length) &&
         (*(t_Cl_Digital_Array->F_P_t_P_t_en_i_Digits
              [sh_Digital_Array_Least_Nonzero_Digit]) == i_0))
    sh_Digital_Array_Least_Nonzero_Digit++;

  // Ненулевые разряды числа переписываются со смещением в младшие разряды,
  // а на из местах образуются нули.
  for (unsigned short ush = sh_Digital_Array_Least_Nonzero_Digit;
       ush < t_Cl_Digital_Array->F_ush_Length; ush++)
  {
    *(t_Cl_Removed_Terminating_Zeroes_Digital_Array->
        F_P_t_P_t_en_i_Digits[ush - sh_Digital_Array_Least_Nonzero_Digit]) =
          *(t_Cl_Digital_Array->F_P_t_P_t_en_i_Digits[ush]);

    *(t_Cl_Removed_Terminating_Zeroes_Digital_Array->
        F_P_t_P_t_en_i_Digits[ush]) = i_0;
  } // for

  // Возврат нового числа.
  return t_Cl_Removed_Terminating_Zeroes_Digital_Array;
} // fn_P_t_Cl_Remove_t_Cl_Terminating_Zeroes


