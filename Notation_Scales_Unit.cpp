// Название:  fn_P_t_P_t_en_i_New.
// Класс:     T_Cl_Digital_Array.
// Описание:  выделение динамической памяти
//            под массив разрядов числа и инициализация их нулями.
//------------------------------------------------------------------------------
void T_Cl_Digital_Array :: fn_P_t_P_t_en_i_New()
{
  // Если определён (не равен NULL) указатель на адрес
  // начала массива разрядов числа, то число уже существует (память не зачищена)
  // и, возможно, определены какие-то указатели на цифры,
  // то занятую память надо освободить.
  if (F_P_t_P_t_en_i_Digits != NULL)
    // Освобождение динамической памяти из-под массива разрядов числа.
    fn_P_t_P_t_en_i_Delete();

  // Выделениее непрерывной облати динамической памяти
  // под размещение указателей на цифры числа.
  F_P_t_P_t_en_i_Digits = new T_P_t_en_i_Figure[F_ush_Length];

  // Выделяются разряды под число, которые инициализируются нулями.
  for (unsigned short ush = 0; ush < F_ush_Length; ush++)
    F_P_t_P_t_en_i_Digits[ush] = new T_en_i_Figure(i_0);
} // T_Cl_Digital_Array :: fn_P_t_P_t_en_i_New

