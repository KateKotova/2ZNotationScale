// Ф у н к ц и я .
//------------------------------------------------------------------------------
// Название:  fn_ush_Assign_as_String.
// Класс:     T_Cl_Notation_Scale_Number_Whole_Part.
// Описание:  инициализиция разрядов массива числа символами цифр,
//            начиня с заданного младшего разряда, из строки,
//            количество которых определяется максимальной точностью числа.
// Параметры:
//   b_Positive_Sign_Flag        - признак положительности числа.
//   as_String                   - некоторая строка цифр,
//                                 назначающая новое значение числа.
//   ush_Least_Significant_Digit - младший значащий разряд числа,
//                                 с которого начинается запись.
// Результат: ответ на вопрос: осуществим ли перевод числа,
//            представленного в заданной строке, в десятичную систему счисления:
//   0 - да;
//   1 - нет, строка содержит недопустимые смволы;
//   2 - да, но возникает переполнение, то есть полученное число
//       не помещается в отведённые ему разряды;
//   3 - нет при прочих ошибках.
//------------------------------------------------------------------------------
unsigned char T_Cl_Notation_Scale_Number_Whole_Part :: fn_ush_Assign_as_String
  (const bool &b_Positive_Sign_Flag,
         AnsiString as_String,
         unsigned short ush_Least_Significant_Digit)
{
  // Удаление в заданной строке ведущих и завершающих пробелов
  // и завершающих нулей, усечение строки до длины,
  // определённой точностью представления числа плюс один символ
  // для округления; если строка окажется пустой или оставшиеся символы
  // не входят во множество цифр данной системы счисления,
  // то число устанавливается в нуль.
  if ((! F_P_t_Cl_Notation_Scale->
           fn_b_Get_as_Correct_Figures_String_Flag(as_String)) ||
      (as_String == ""))
  {
    // Установка числа в нуль (+0).
    pr_b_Zero_Flag = 0;
    // Возврат кода ошибки - 1.
    return 1;
  } // if

  // Если длина фрагмента, куда вставляются символы из заданной строки,
  // окажется меньше её длины (в случае большого номера
  // младшего записываемого разра) или начальная позиция вставки
  // окажется больше старшего разряда числа,
  // то число устанавливается в состояние переполнения.
  if ((F_P_t_Cl_Digital_Array->pr_ush_Length - ush_Least_Significant_Digit) <
       as_String.Length())
     // Входит в предыдущее условие:
     // || (ush_Least_Significant_Digit >
     //    (F_t_Cl_Digital_Array->pr_ush_Length - 1)).
  {
    // Установка числа в состояние переполнения.
    F_b_Overflow_Flag = true;
    // Возврат из функции.
    return 2;
  } // if

  // Разряды числа обнуляются.
  pr_b_Zero_Flag = true;

  // Далее разряды числа поля для всавки символов из заданной строки,
  // инициализируются цифрами, эквивалентными её символам.
  // Просмотр всех разрядов числа в строке цифр от младшего разряда,
  // расположенного в конце строки, до старшего - в начале,
  // получение индекса разряда числа, записанного в строке.
  for (int i_String_Char_Index   = as_String.Length(),
           i_Digital_Array_Digit = ush_Least_Significant_Digit;
       i_String_Char_Index > 0;
       i_String_Char_Index--, i_Digital_Array_Digit++)
  {
    // Цифра данной системы счисления, эквивалентная текущему символу.
    T_en_i_Figure t_en_i_String_Char_Figure;

    // Поиск цифры-эквивалента в данной системе счисления
    // для текущего симаола заданной строки.
    F_P_t_Cl_Notation_Scale->fn_b_Get_ch_Char_t_en_i_Figure
      (as_String[i_String_Char_Index], t_en_i_String_Char_Figure);

    // Инициализация разряда числа полученным числовым представлением
    // символа заданной строки.
    *(F_P_t_Cl_Digital_Array->F_P_t_P_t_en_i_Digits[i_Digital_Array_Digit]) =
        t_en_i_String_Char_Figure;
  } // for

  // Установка признака положительности порядка числа.
  F_b_Positive_Sign_Flag = b_Positive_Sign_Flag;

  // Ложность переполнения порядка.
  F_b_Overflow_Flag = false;

  // Округление числа до длины максимальной мантиссы.
  fn_Get_t_a_P_t_en_i_Rounded
    (F_P_t_Cl_Notation_Scale->pr_uch_Number_Mantissa_Length);

  // Возврат признака успешного завершения функции.
  return 0;
} // T_Cl_Notation_Scale_Number_Whole_Part :: fn_ush_Assign_as_String

