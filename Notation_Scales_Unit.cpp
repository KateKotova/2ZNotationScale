  if (ush_Length == 0)
    F_ush_Length = 1;

  else
    // Если длина числа превышает максимальную длину целой части числа
    // в двоичной системе счисления в ненормализованном виде,
    // то она заменяется на это максимально допустимое значение.
    if (ush_Length > ush_Binary_Notation_Scale_Whole_Part_Length)
      F_ush_Length = ush_Binary_Notation_Scale_Whole_Part_Length;

    else
      // Зпись длины числа.
      F_ush_Length = ush_Length;

  // Указатель на адрес начала массива разрядов числа не определён.
  F_P_t_P_t_en_i_Digits = NULL;

  // Выделение динамической памяти под массив разрядов числа заданной длины
  // и инициализация их нулями.
  fn_P_t_P_t_en_i_New();
