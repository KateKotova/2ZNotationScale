// К о н с т а н т н а я   ф у н к ц и я .
//------------------------------------------------------------------------------
// Название:  fn_b_Get_as_Correct_Figures_String_Flag.
// Класс:     T_Cl_Notation_Scale.
// Описание:  удаление в заданной строке последовательности символов
//            ведущих и завершающих пробелов и ведущих нулей,
//            усечение строки до длины максимальной мантиссы плюс один символ
//            для округления и получение признака вхождения
//            оставшихся символов во множество цифр данной системы счисления.
// Параметры:
//   as_String - некоторая строка, чьи символы проверяются на идентичность
//               символам цифр данной системы счисления.
// Результат: ответ на вопрос: входят ли все символы заданной строки
//            в алфавит данной системы счисления.
//------------------------------------------------------------------------------
bool T_Cl_Notation_Scale :: fn_b_Get_as_Correct_Figures_String_Flag
  (AnsiString &as_String) const
{
  // Удаление ведущих и завершающих пробелов в заданной строке.
  as_String = as_String.Trim();

  // Если заданная строка оказывается пустой,
  // то не содержит символов алфавита данной системы счисления.
  if (as_String == "")
    // Возврат отрицательного ответа.
    return false;

  // Удаление ведущих нулей в заданной строке,
  // чтобы при цифра старшего разряда оказалась значищей -
  // всякий раз в начале заданной строки удаляется нуль,
  // пока там не обнаружится символ, отличный от нуля,
  // или не закончится заданная строка.
  while ((as_String != "") && (as_String[1] == '0'))
    // При обнаружения нуля в начале заданной строки
    // она становится короче на один символ.
    as_String = as_String.SubString(2, as_String.Length() - 1);

  // Если заданная строка оказывается пустой, то она содержала только нули,
  // которые входят в алфавит каждой системы счислния.
  if (as_String == "")
    // Возврат положительного ответа.
    return true;

  // Если количество символов в заданной строке больше,
  // чем длина максимальной мантиссы в даноной системе счисления плюс один,
  // то отсекаются символы конца заданной строки,
  // соответствующие младшим разрадам, длина заданной строки становится
  // на один символ больше количества символов, чем длина максимальной мантиссы:
  // неусечённый символ, ставший последним в заданной строке
  // оставлен для округления.
  if ((as_String.Length() - 1) > F_uch_Number_Mantissa_Length)
    // Усечение длины строки до количества символов,
    // определённого длиной максимальной мантиссы плюс один.
    as_String.SetLength(F_uch_Number_Mantissa_Length + 1);

  // Счётчик символов заданной строки,
  // отсчёт ведётся от первого символа к последним.
  unsigned char uch = 1;

  // Пока текущий символ входит в алфавит данной системы счисления
  // и номер текущего символа не стал равным длине заданной строки
  // (то есть пока не просмотрены все символы заданной строки,
  // которые нумеруются, начиная с единицы),
  // осуществсяется переход для просмотра следующего символа.
  while ((uch <= as_String.Length()) &&
          F_t_se_ch_Figures.Contains(as_String[uch]))
    uch++;

  // Если последний номер символа больше длины заданной строки,
  // то были просмотрены все символы заданной строки от первого до последнего,
  // но среди них не было обнаружено ни одного, не пинадлежащего множеству цифр
  // данной системы счисления, то заданная строка состоит из символов,
  // определённых в алфавите данной системы счисления, иначе - нет.
  if (uch > as_String.Length())
    // Возврат положиельного ответа.
    return true;
    
  else
    // Возврат отрицательного ответа.
    return false;
} // T_Cl_Notation_Scale :: fn_b_Get_as_Correct_Figures_String_Flag


//==============================================================================
// К о н с т а н т н а я   ф у н к ц и я .
//------------------------------------------------------------------------------
// Название:  fn_b_Get_t_Cl_Correct_Figures_Digital_Array_Flag.
// Класс:     T_Cl_Notation_Scale.
// Описание:  получение признака вхождения всех цифр
//            из разрядов некоторого числа во множество цифр
//            данной системы счисления.
// Параметры:
//   t_Cl_Digital_Array - массив разрядов некоторого числа, содержащих цифры,
//                        определённые в системе счисления
//                        от двоичной до тридцати шестиричной.
// Результат: ответ на вопрос: все ли цифры данного числа определены
//            в данной системе счисления.
//------------------------------------------------------------------------------
bool T_Cl_Notation_Scale :: fn_b_Get_t_Cl_Correct_Figures_Digital_Array_Flag
  (T_Cl_Digital_Array *t_Cl_Digital_Array) const
{
  // Если не определённости (равен NULL) указатель на адрес
  // начала массива разрядов числа или хотя бы один указатель на цифру,
  // то значение такой цифры не определено
  // и она не принадлежит множеству цифр данной системы счисления.
  if (t_Cl_Digital_Array->pr_b_NULLs_Flag)
    // Возврат отрицательного ответа.
    return false;

  // Счётчик разрядов заданного числа,
  // отсчёт ведётся от младшего разряда к старшим.
  unsigned short ush = 0;

  // Пока цифра текущего разряда принадлежит множеству цифр
  // данной системы счисления и текущий разряд не стал равным
  // длине числа (то есть пока не просмотрены все разряды числа,
  // которые нумеруются, начиная с нуля),
  // осуществсяется переход для просмотра следующего разряда.
  while ((ush < t_Cl_Digital_Array->pr_ush_Length) &&
         (F_t_se_ch_Figures.Contains
            (AnsiString(*t_Cl_Digital_Array->F_P_t_P_t_en_i_Digits[ush])[1])))
    ush++;

  // Если последний номер разряда равен длине числа, то были просмотрены
  // все разряды числа от нулевого до (F_ush_Length - 1)-го,
  // но среди них не было обнаружено ни одного, не принадлежащего множеству цифр
  // данной системы счисления, то число состоит из цифр,
  // определённых в данной системе счисления, иначе - нет.
  if (ush == t_Cl_Digital_Array->pr_ush_Length)
    // Возврат положиельного ответа.
    return true;

  else
    // Возврат отрицательного ответа.
    return false;
} // T_Cl_Notation_Scale :: fn_b_Get_t_Cl_Correct_Figures_Digital_Array_Flag


