// Ф у н к ц и я.
//------------------------------------------------------------------------------
// Название:  fn_uch_Binary_Operation.
// Класс:     T_Cl_Notation_Scale_Number_Order.
// Описание:  бинарная операция - операция, задействующая два аргумента.
// Параметры:
//   t_en_i_Operation_Type                      - вид операции: равенство,
//                                                сложение,  вычитание,
//                                                умножение, деление,
//                                                удаление символа с конца,
//                                                обнуление. 
//   as_Function_String                         - строковое представление
//                                                функции от аргументов.
//   as_Number_String_1                         - строковое представление
//                                                первого аргумента.
//   t_en_i_Number_String_Notation_Scale_Base_1 - система счисления
//                                                первого аргумента.
//   as_Number_String_2                         - строковое представление
//                                                второго аргумента
//                                                (по умолчанию - нуль).
//   t_en_i_Number_String_Notation_Scale_Base_2 - система счисления
//                                                второго аргумента
//                                                (по умолчанию - десятичная).
// Результат: ответ на вопрос: осуществима ли опрация над заданными аргументами:
//   0 - да;
//   1 - да, но полученное значение функции превышет
//       максимально доустимое число.
//   2 - нет, фактически: возникает переполнение.
//------------------------------------------------------------------------------
unsigned char fn_uch_Binary_Operation
  (const T_en_i_Operation_Type &t_en_i_Operation_Type,
         AnsiString    &as_Function_String,

         AnsiString    as_Number_String_1,
   const T_en_i_Figure &t_en_i_Number_String_Notation_Scale_Base_1,

         AnsiString    as_Number_String_2,
   const T_en_i_Figure &t_en_i_Number_String_Notation_Scale_Base_2)
{
  // Выделение динамической памяти под систему счисления первого аргумента
  // и её инициализация, выделение динамической памяти под массив разрядов
  // первого аргумента и инициализация этих разрядов
  // заданным строковым представлением первого аргумента.
  T_Cl_Notation_Scale_Number_Order *P_t_Cl_Notation_Scale_Number_Order_1 =
    new T_Cl_Notation_Scale_Number_Order
          (as_Number_String_1, t_en_i_Number_String_Notation_Scale_Base_1);

  // Выделение динамической памяти под систему счисления второго аргумента
  // и её инициализация, выделение динамической памяти под массив разрядов
  // второго аргумента и инициализация этих разрядов
  // заданным строковым представлением второго аргумента.
  T_Cl_Notation_Scale_Number_Order *P_t_Cl_Notation_Scale_Number_Order_2 =
    new T_Cl_Notation_Scale_Number_Order
          (as_Number_String_2, t_en_i_Number_String_Notation_Scale_Base_2);

  // Выделение динамической памяти под систему счисления второго аргумента
  // и её инициализация, выделение динамической памяти под массив разрядов
  // функции от чисел и инициализация этих разрядов нулями.
  T_Cl_Notation_Scale_Number_Order
    *P_t_Cl_Function_Notation_Scale_Number_Order =
       new T_Cl_Notation_Scale_Number_Order
             (t_en_i_Number_String_Notation_Scale_Base_2);

  // Выделение динамической памяти под максимальный порядока числа
  // и его инициализация, если он ещё не был определён.
  fn_P_t_Cl_Maximum_Notation_Scale_Number_Order_New();

  // Присвоение разрядам массива числа функции от заданных чисел значения,
  // полученного при выполнении выборанной операции, устновленной по её типу.
  switch (t_en_i_Operation_Type)
  {
    // Равенство.
    case i_EQUALITY :
    {
      // Присвоение второго из аргументов.
      *P_t_Cl_Function_Notation_Scale_Number_Order =
         *P_t_Cl_Notation_Scale_Number_Order_2;

      // Переход в систему счисления первого аргумента.
      P_t_Cl_Function_Notation_Scale_Number_Order->
        pr_t_en_i_Notation_Scale_Base = P_t_Cl_Notation_Scale_Number_Order_1->
                                          pr_t_en_i_Notation_Scale_Base;
    } // case i_EQUALITY
    break;

    // Cложение.
    case i_ADDING :
      // Cуммa аргументов.
      P_t_Cl_Function_Notation_Scale_Number_Order =
        *P_t_Cl_Notation_Scale_Number_Order_1 +
        *P_t_Cl_Notation_Scale_Number_Order_2;
    break;

    // Вычитание.
    case i_SUBTRACTION :
      // Разность аргументов.
      P_t_Cl_Function_Notation_Scale_Number_Order =
        *P_t_Cl_Notation_Scale_Number_Order_1 -
        *P_t_Cl_Notation_Scale_Number_Order_2;
    break;

    // Умножение.
    case i_MULTIPLYING :
      // Произведение аргументов.
      P_t_Cl_Function_Notation_Scale_Number_Order =
        *P_t_Cl_Notation_Scale_Number_Order_1 *
        *P_t_Cl_Notation_Scale_Number_Order_2;
    break;

    // Деление.
    case i_DIVISION :
      // Частное аргументов.
      P_t_Cl_Function_Notation_Scale_Number_Order =
        *P_t_Cl_Notation_Scale_Number_Order_1 /
        *P_t_Cl_Notation_Scale_Number_Order_2;
    break;

    default : ;
  } // switch

  // Код ошибки пока ошибок нет.
  unsigned char uch_Error_Code = 0;

  // Если возникло переполнение, то операция завершена неудачно.
  if (P_t_Cl_Function_Notation_Scale_Number_Order->pr_b_Overflow_Flag)
  {
    // Код ошибки переполнения.
    uch_Error_Code = 2;
    // Отображаемая строка.
    as_Function_String = as_Overflow_Error_String;
  } // if

  else
    // Если число по модулю оказалось больше максимально допустимого,
    // то операция завершена неудачно.
    if (*P_t_Cl_Function_Notation_Scale_Number_Order->
           fn_P_t_Cl_Get_t_Cl_Absolute_Value() >
        *P_t_Cl_Maximum_Notation_Scale_Number_Order)
    {
      // Код ошибки превышения максимально допустимого значения.
      uch_Error_Code = 1;
      // Отображаемая строка.
      as_Function_String = as_Maximum_Excess_Error_String;
    } // if

    else
      // Пробразование полученной функции в строку.
      as_Function_String = P_t_Cl_Function_Notation_Scale_Number_Order->
                             fn_sh_Get_t_Cl_String_Equivalent();

  // Освобождение динамической памяти.
  // Удаление первого числа.
  delete P_t_Cl_Notation_Scale_Number_Order_1;
  P_t_Cl_Notation_Scale_Number_Order_1 = NULL;

  // Удаление второго числа.
  delete P_t_Cl_Notation_Scale_Number_Order_2;
  P_t_Cl_Notation_Scale_Number_Order_2 = NULL;

  // Удаление функции этих двух чисел.
  delete P_t_Cl_Function_Notation_Scale_Number_Order;
  P_t_Cl_Function_Notation_Scale_Number_Order = NULL;

  // Возврат кода ошибки.
  return uch_Error_Code;
} // fn_uch_Binary_Operation


//==============================================================================
// Ф у н к ц и я.
//------------------------------------------------------------------------------
// Название:  fn_as_Calculator_Operation.
// Класс:     T_Cl_Notation_Scale_Number_Order.
// Описание:  выполнение операций, адаптированных для калькулятора.
// Параметры:
//   t_en_i_Operation_Type - вид операции: равенство, сложение,  вычитание,
//                           умножение, деление, удаление символа с конца,
//                           обнуление, установка новой системы счисления.
//   as_Current_String     - строковое представление текущего числа.
// Результат: функция - число, полученное в результате операции над числом,
//            хранящимся в памяти, и переданным в качестве параметра
//            текущим числом.
//------------------------------------------------------------------------------
AnsiString fn_as_Calculator_Operation
  (const T_en_i_Operation_Type &t_en_i_Operation_Type,
   const AnsiString            &as_Current_String,
   const T_en_i_Figure         &t_en_i_Current_String_Notation_Scale_Base)
{
  // строка функции для вывода результата.
  AnsiString as_Function_String = "";

  // Если операция имеет тип установки новой системы счисления,
  // то при её выполнении не возникнет предусмотренных ошибок,
  // поэтому запись результата производится напрямую
  // через параметр фцункии операции равенства для указанных аргументов.
  // Текущее число переписывается в новой системе счисления,
  // и число из памяти также устанавливается в эту систему счисления.
  if (t_en_i_Operation_Type == i_SET_NOTATION_SCALE)
  {
    // В результате операции, выполненной выше в строке-результате
    // находится число, равное числу из памяти, но в новой системе счисления.
    fn_uch_Binary_Operation
      (i_EQUALITY,              as_Memory_Number_String,
       as_Current_String, t_en_i_Current_String_Notation_Scale_Base,
       as_Memory_Number_String, t_en_i_Current_Notation_Scale_Base);

    // Повтор операции для текущего числа - при смене мест аргументов:
    // получение текущего числа в новой системе счисления
    fn_uch_Binary_Operation
      (i_EQUALITY,              as_Function_String,
       as_Memory_Number_String, t_en_i_Current_String_Notation_Scale_Base,
       as_Current_String, t_en_i_Current_Notation_Scale_Base);

    // Максимальный порядок также переводится в нновую систему счисления.
    P_t_Cl_Maximum_Notation_Scale_Number_Order->F_P_t_Cl_Notation_Scale->
      pr_t_en_i_Base = t_en_i_Current_String_Notation_Scale_Base;

    // Текущая система счисления (глобальная переменная) также меняется
    t_en_i_Current_Notation_Scale_Base =
      t_en_i_Current_String_Notation_Scale_Base;

    // Возврат текущего числа в новой системе счисления.
    return as_Function_String;
  } // if

  // При выполнении остальных операций
  // аргуенты находятся в одной системе счисления - в текущей.
  // Код ошибки, сгенерированный при выполнении операции.
  unsigned char uch_Error_Code = fn_uch_Binary_Operation
    (t_en_i_Operation_Type,   as_Function_String,
     as_Memory_Number_String, t_en_i_Current_Notation_Scale_Base,
     as_Current_String, t_en_i_Current_Notation_Scale_Base);

  // Выполнение выборанной операции, устновленной по её типу.

  // Равенство, здесь имеет другой смысл, нежели, чем в предыдущей функции:
  // не происходит преобразования и приравнивания чисел,
  // после любой другой операции результат и так подсчитывается и выводится;
  // Здесь это символизирует начало новой операции и, как следствие,
  // обновление памяти с занесением в неё нового числа.
  if (t_en_i_Operation_Type == i_EQUALITY)
  {
    // Запись после выполнения операции текущего числа в память и его вывод.
    as_Memory_Number_String = as_Function_String;
    return as_Memory_Number_String;
  } // if  

  // Cложение, вычитание, умножение, деление.
  // Если операция завершена без ошибок (коод ошибки не равен нулю).
  if (! uch_Error_Code)
    as_Memory_Number_String = as_Function_String;

  // Иначе число сохраняет нуль.
  else
    as_Memory_Number_String = '0';

  // Возвращается для вывода строка результата функции.
  return as_Function_String;
} // fn_as_Calculator_Operation


