// Название:  fn_P_t_Cl_Add_t_Cl_Leading_Zeroes.
// Класс:     T_Cl_Digital_Array.
// Описание:  увеличение длины массива разрядов числа
//            за счёт добавления ведущих нулей.
// Параметры:
//   t_Cl_Digital_Array - массив разрядов числа, куда добавляются ведущие нули.
//   ush_New_t_Cl_Digital_Array_Length -
//     новая длина переданного в качестве параметра массива разрядов числа,
//     полученная в результате добавления ведущих нулей.
// Результат: данный массив разрядов числа с добавленными ведущими нулями.
//------------------------------------------------------------------------------
T_Cl_Digital_Array *fn_P_t_Cl_Add_t_Cl_Leading_Zeroes
  (T_Cl_Digital_Array *t_Cl_Digital_Array,
   unsigned short ush_New_t_Cl_Digital_Array_Length)
{
  // Новое число - массив разрядов данного числа c ведущиvb нулями.
  T_Cl_Digital_Array *t_Cl_Added_Leading_Zeroes_Digital_Array =
    new T_Cl_Digital_Array(ush_New_t_Cl_Digital_Array_Length);

  // Копирование значения числа в начало нового.
  for (unsigned short ush = 0;
       ush < t_Cl_Digital_Array->F_ush_Length; ush++)
    t_Cl_Added_Leading_Zeroes_Digital_Array->F_P_t_P_t_en_i_Digits[ush] =
      t_Cl_Digital_Array->F_P_t_P_t_en_i_Digits[ush];

  // Добавдение нулей в конец числа.
  for (unsigned short ush = t_Cl_Digital_Array->F_ush_Length;
       ush < ush_New_t_Cl_Digital_Array_Length; ush++)
    // Выделение динимической памяти под разряд и инициализация его нулём.
    t_Cl_Added_Leading_Zeroes_Digital_Array->F_P_t_P_t_en_i_Digits[ush] =
      new T_en_i_Figure(i_0);

  // Увеличение длины нового числа.
  t_Cl_Added_Leading_Zeroes_Digital_Array->F_ush_Length =
    ush_New_t_Cl_Digital_Array_Length;

  // Возврат нового числа.
  return t_Cl_Added_Leading_Zeroes_Digital_Array;
} // fn_P_t_Cl_Add_t_Cl_Leading_Zeroes


