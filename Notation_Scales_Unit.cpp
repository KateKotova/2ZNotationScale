// Д р у ж е с т в е н н а я   ф у н к ц и я .
//------------------------------------------------------------------------------
// Название:  fn_P_t_Cl_Invert_t_Cl_Digits.
// Класс:     T_Cl_Digital_Array.
// Описание:  инвертирование массива разрядов числа -
//            пререстановка разрядов в обратном порядке.
// Параметры:
//   t_Cl_Digital_Array - массив разрядов числа, чьи разряды инвертируются.
// Результат: данный массив разрядов числа с разрядами,
//            расположенными в обратном порядке.
//------------------------------------------------------------------------------
T_Cl_Digital_Array *fn_P_t_Cl_Invert_t_Cl_Digits
  (T_Cl_Digital_Array *t_Cl_Digital_Array)
{
  // Новое число - массив разрядов данного числа
  // c инвертированной последовательностью разрядов.
  T_Cl_Digital_Array *t_Cl_Inverted_Digits_Digital_Array =
    new T_Cl_Digital_Array();
  // Копирование значения числа в новое.
  *t_Cl_Inverted_Digits_Digital_Array = *t_Cl_Digital_Array;

  // Копирование разрядов с начала до конца данного числа
  // в разряды с конца в начало нового числа.
  for (unsigned short ush_Digital_Array_Digit = 0,
                      ush_Inverted_Digits_Digital_Array_Digit =
                        t_Cl_Inverted_Digits_Digital_Array->F_ush_Length - 1;
       ush_Digital_Array_Digit < t_Cl_Digital_Array->F_ush_Length;
       ush_Digital_Array_Digit++, ush_Inverted_Digits_Digital_Array_Digit--)

    // Если текущий разряд данного числ не определён,
    // то аналогичный перекрёстный разряд нового числа
    // также должен быть не опеределён.
    if (t_Cl_Digital_Array->F_P_t_P_t_en_i_Digits[ush_Digital_Array_Digit] ==
          NULL)
      t_Cl_Inverted_Digits_Digital_Array->
        F_P_t_P_t_en_i_Digits[ush_Inverted_Digits_Digital_Array_Digit] = NULL;

    // Иначе цифра числа копируется
    // в соответствующую перекрёстную цифру нового числа.
    else
      *(t_Cl_Inverted_Digits_Digital_Array->
          F_P_t_P_t_en_i_Digits[ush_Inverted_Digits_Digital_Array_Digit]) =
            *(t_Cl_Digital_Array->
                F_P_t_P_t_en_i_Digits[ush_Digital_Array_Digit]);

  // Удаление завершающих нулей путём сдвига значищих разрядов.
  t_Cl_Inverted_Digits_Digital_Array =
    fn_P_t_Cl_Remove_t_Cl_Terminating_Zeroes
      (t_Cl_Inverted_Digits_Digital_Array);

  // Возврат нового числа.
  return t_Cl_Inverted_Digits_Digital_Array;
} // fn_P_t_Cl_Invert_t_Cl_Digits


