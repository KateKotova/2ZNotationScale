// Результат: ответ на вопрос: найдена ли в данной системе счисления
//            цифра-эквивалент для заданного символа.
//------------------------------------------------------------------------------
bool T_Cl_Notation_Scale :: fn_b_Get_ch_Char_t_en_i_Figure
  (const char &ch_Char,
   T_en_i_Figure &t_en_i_Figure) const
{
  // Проверка вхождения заданного символа во множество символьных представлений
  // цифр алфавита данной системы счисления.
  if (F_t_se_ch_Figures.Contains(ch_Char))
  {
    // Счётчик символов константного массива символьных представлений цифр
    // систем счисления от двоичной до тридцати семиричной,
    // отсчёт ведётся от нулевого символа к последним.
    unsigned char uch_Figure_Index = 0;

    // Пока заданный символ не равен текущему символу
    // из массива цифр всех систем счисления
    // осуществсяется переход к следующемудля элементу этого массива.
    while (ch_Char != a_ch_All_Figures[uch_Figure_Index])
      uch_Figure_Index++;

    // Заданная цифра - это полученный индекс символа
    // из массива символьных представлений цифр всех систем счисления.
    t_en_i_Figure = (T_en_i_Figure) uch_Figure_Index;

    // Возврат положительного ответа.
    return true;
  } // if

  // Неуспешное завершение функции - цифра-эквивалент не найдена.
  else
  {
    // Запись нуля в заданную цифру, хотя это значение не имеет смысла,
    // ибо нуль не эквивалентен данному символу,
    // поскольку для него нет эквивалента в данной системе счисления.
    t_en_i_Figure = i_0;

    // Возврат отрицательного ответа.
    return false;
  } // else
} // T_Cl_Notation_Scale :: b_Get_ch_Char_t_en_i_Figure


